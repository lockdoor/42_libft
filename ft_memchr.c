/*
	The memchr() function locates the first occurrence of c (converted to an
	unsigned char) in string s.
*/
#include <stdlib.h>

char	*ft_memchr(const void *s, int c, size_t n)
{
	while (n--)
	{
		if (*(unsigned char *)s == (unsigned char)c)
			return ((char *) s);
		s++ ;
	}
	return (NULL);
}
